            <p>Conformément au Règlement Général sur la Protection des Données (RGPD), nous collectons uniquement les données nécessaires à la gestion des inscriptions, à la prise de rendez-vous et au suivi pédagogique.</p>

        <h3>Données collectées</h3>
            <ul>
                <li>Identité : nom, prénom</li>
                <li>Coordonnées : adresse postale, e‑mail, téléphone</li>
                <li>Données pédagogiques : dates de cours, résultats aux tests</li>
                <li>Données administratives pour facturation : N° de pièce d'identité, SIRET quand nécessaire</li>
