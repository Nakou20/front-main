        <h3>Finalités</h3>
            <p>Les données sont utilisées pour : l'inscription et le suivi des élèves, la facturation, la planification des rendez‑vous, et l'envoi d'informations relatives à nos services.</p>

        <h3>Durée de conservation</h3>
            <p>Les données personnelles sont conservées pendant la durée nécessaire aux finalités pour lesquelles elles ont été collectées, conformément à la législation en vigueur. Les durées précises (ex. documents comptables, dossiers pédagogiques) peuvent être définies par la réglementation : adaptez-les selon votre situation.</p>

        <h3>Partage et sécurité</h3>
            <p>Les données ne sont pas vendues. Elles peuvent être partagées avec des prestataires techniques (hébergeur, solution de paiement) uniquement dans le cadre strict de la prestation et avec des contrats garantissant la protection des données. Nous appliquons des mesures techniques et organisationnelles pour protéger les données (accès restreint, sauvegardes, chiffrement lorsque nécessaire).</p>

